function logUserAction(userAction) {
    console.log(`User performed: ${userAction}`);
}

// Logging user actions
logUserAction("Login");                 // Logs: User performed: Login
logUserAction("View Profile");          // Logs: User performed: View Profile
logUserAction("Logout");                 // Logs: User performed: Logout
logUserAction("Add Item to Cart");      // Logs: User performed: Add Item to Cart
